package net.mahdilamb.colormap.color;

/**
 * The type of color as used by the {@link net.mahdilamb.colormap.color.NewColor} annotation
 */
public enum ColorType {
    /**
     * Color present in JavaSE's AWT
     */
    AWT,
    /**
     * Color from CSS4
     */
    CSS,
    /**
     * Color from the Tableau package
     */
    TABLEAU

}